    {
        $url_person_id = $request->person_id;
        if($url_person_id)
            $people = Person::find($url_person_id)->get();
        else
            $people = Person::all();

        $documentTypes = DocumentType::all();

        //dd($people);
        
        return view('document.create', [
             'url_person_id'=>$url_person_id, 
             'people'=>$people,
             'documentTypes'=>$documentTypes
            ]);
    }

